#include <stdio.h>    // Entrada y salida estándar
#include <stdlib.h>   // Funciones de utilidad estándar

int main() {
    int numero = 98;  // Declaración e inicialización de 'numero'

    // Comprobar si 'numero' es menor que 50 e imprimir el resultado
    if (numero < 50) {
        printf("El numero es menor que 50\n");
    } else {
        printf("El numero es mayor que 50\n");
    }

    printf("Fin del programa\n");  // Mensaje de finalización
    printf("\n");  // Línea en blanco

    return 0;  // Salida del programa
}
