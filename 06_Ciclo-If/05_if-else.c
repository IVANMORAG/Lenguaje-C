#include <stdio.h>
#include <stdlib.h>

int main()
{
    // Definición de variable
    int numero = 36;
    
    // Verificar si el número es menor que 50
    if (numero < 50)
    {
        printf("El numero es menor que 50\n");
    }
    else // Si el número no es menor que 50, entonces es mayor o igual
    {
        printf("El numero es mayor que 50\n");
    }

    // Mensaje de finalización
    printf("Fin del programa\n");
    printf("\n");
    
    return 0;
}
